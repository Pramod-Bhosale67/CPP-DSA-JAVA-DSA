// class
public class Main{
	
	// main function - where execution start
	public static void main(String[] args) {

		// Types of Data Types

		// Integer Data Types

		int x = 10;
		System.out.println("Value of the x: " + x);		

		short y = 100;
		System.out.println("Value of the y: " + y);	

		byte z = 100;
		System.out.println("Value of the z: " + z);	

		long a = 65535631;
		System.out.println("Value of the a: " + a);	


		// Boolean
		boolean isTrue = true;
		System.out.println("Value of the isTrue: " + isTrue);	


		// char
		char ch = '+';  // should contain single char
		System.out.println("Value of the ch: " + ch);	

		// floating points
		float b = 14.23F;
		System.out.println("Value of the b: " + b);	


		double d = 45.90;
		System.out.println("Value of the d: " + d);	





    }
}